package com.example.dhirajchhabraeng.lecture4_part2;

public class Display {

    String mNumber = "";
    String mSquare = "";

    public Display(String number, String square) {
        mNumber = number;
        mSquare = square;
    }

    public void setmNumber(String mNumber) {
        this.mNumber = mNumber;
    }

    public void setmSquare(String mSquare) {
        this.mSquare = mSquare;
    }

    public String getmNumber() {
        return mNumber;
    }

    public String getmSquare() {
        return mSquare;
    }
}

